# VARIABLES
a = 10
b = 3

# ----------------------
# Operadores aritméticos
# ----------------------
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
division_entera = a // b
modulo = a % b
potencia = a ** b

# ----------------------
# Operadores de comparación
# ----------------------
es_igual = a == b       # False
es_diferente = a != b   # True
mayor = a > b           # True
menor = a < b           # False
mayor_igual = a >= b    # True
menor_igual = a <= b    # False

# ----------------------
# Operadores de asignación
# ----------------------
c = 5
c +=2   # equivalente a c = c + 2
c -=1   # equivalente a c = c - 1
c *= 3   # equivalente a c = c * 3
c /= 2   # equivalente a c = c / 2
c %= 3   # equivalente a c = c % 3
c **= 2  # equivalente a c = c ** 2
c //= 2  # equivalente a c = c // 2
